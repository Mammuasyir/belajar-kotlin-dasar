fun main() {
    //>
    val number1 = 1
    val number2 = 2
    println(number1 > number2)
    //<
    println(number1 < number2)
    //>=
    println(number1 >= number2)
    //<=
    println(number1 <= number2)
    //==
    println(number1 == number2)
    //!= (tidak sama dengan)
    println(number1 != number2)

}